describe('PostgreSQL - ON CONFLICT', () => {
  test('parse_on_conflict_do_nothing', () => {
    parseOne('INSERT INTO t VALUES (1) ON CONFLICT DO NOTHING', pg);
    parseOne('INSERT INTO t VALUES (1) ON CONFLICT (id) DO NOTHING', pg);
    parseOne('INSERT INTO t VALUES (1) ON CONFLICT ON CONSTRAINT pk DO NOTHING', pg);
  });

  test('parse_on_conflict_do_update', () => {
    parseOne('INSERT INTO t VALUES (1) ON CONFLICT (id) DO UPDATE SET col = 1', pg);
    parseOne('INSERT INTO t VALUES (1) ON CONFLICT (id) DO UPDATE SET col = EXCLUDED.col', pg);
    parseOne('INSERT INTO t VALUES (1) ON CONFLICT (id) DO UPDATE SET col = 1 WHERE condition', pg);
  });
});

