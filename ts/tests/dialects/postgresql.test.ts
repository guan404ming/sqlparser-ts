describe('PostgreSQL - COPY', () => {
  test('parse_copy_from', () => {
    parseOne("COPY t FROM '/path/to/file.csv'", pg);
    parseOne("COPY t FROM '/path/to/file.csv' WITH (FORMAT csv)", pg);
    parseOne("COPY t FROM '/path/to/file.csv' WITH (FORMAT csv, HEADER true)", pg);
    parseOne("COPY t FROM '/path/to/file.csv' WITH (DELIMITER ',')", pg);
    parseOne("COPY t (col1, col2) FROM '/path/to/file.csv'", pg);
  });

  test('parse_copy_to', () => {
    parseOne("COPY t TO '/path/to/file.csv'", pg);
    parseOne('COPY t TO STDOUT', pg);
    parseOne("COPY t TO PROGRAM 'gzip > /path/to/file.csv.gz'", pg);
    parseOne("COPY (SELECT * FROM t) TO '/path/to/file.csv'", pg);
  });

  test('parse_copy_options', () => {
    parseOne("COPY t FROM '/path/to/file.csv' WITH (FORMAT csv, FREEZE, DELIMITER ',', NULL '', HEADER, QUOTE '\"', ESCAPE E'\\\\', ENCODING 'UTF8')", pg);
  });
});

