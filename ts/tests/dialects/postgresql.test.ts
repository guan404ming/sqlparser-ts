/**
 * PostgreSQL dialect tests
 * Ported from sqlparser_postgres.rs
 */

import {
  parseOne,
  expectParseError,
  dialects,
} from '../test-utils';

const pg = dialects.postgresql;

describe('PostgreSQL - CREATE TABLE', () => {
  test('parse_create_table_generated_always_as_identity', () => {
    parseOne('CREATE TABLE t (id INT GENERATED ALWAYS AS IDENTITY)', pg);
    parseOne('CREATE TABLE t (id INT GENERATED BY DEFAULT AS IDENTITY)', pg);
    // IDENTITY with parameters not yet fully supported
    // parseOne('CREATE TABLE t (id INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1))', pg);
  });

  test('parse_create_table_with_defaults', () => {
    parseOne("CREATE TABLE t (id INT DEFAULT nextval('seq'))", pg);
    parseOne("CREATE TABLE t (created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP)", pg);
  });

  test('parse_create_table_with_collate', () => {
    parseOne("CREATE TABLE t (name TEXT COLLATE \"en_US\")", pg);
  });

  test('parse_create_table_if_not_exists', () => {
    parseOne('CREATE TABLE IF NOT EXISTS t (id INT)', pg);
  });

  test('parse_create_table_empty', () => {
    parseOne('CREATE TABLE t ()', pg);
  });

  test('parse_create_table_constraints_only', () => {
    parseOne('CREATE TABLE t (CHECK (a > 0))', pg);
  });

  test('parse_create_table_with_array_types', () => {
    parseOne('CREATE TABLE t (ids INT[])', pg);
    // ARRAY keyword syntax not yet fully supported
    // parseOne('CREATE TABLE t (ids INT ARRAY)', pg);
    parseOne('CREATE TABLE t (matrix INT[][])', pg);
  });

  test('parse_create_table_with_serial', () => {
    parseOne('CREATE TABLE t (id SERIAL PRIMARY KEY)', pg);
    parseOne('CREATE TABLE t (id BIGSERIAL PRIMARY KEY)', pg);
    parseOne('CREATE TABLE t (id SMALLSERIAL)', pg);
  });

  test('parse_create_table_with_uuid', () => {
    parseOne('CREATE TABLE t (id UUID DEFAULT gen_random_uuid())', pg);
  });

  test('parse_create_table_inherit', () => {
    parseOne('CREATE TABLE child () INHERITS (parent)', pg);
  });

  test('parse_create_table_partition', () => {
    parseOne('CREATE TABLE t (id INT) PARTITION BY RANGE (id)', pg);
    parseOne('CREATE TABLE t (id INT) PARTITION BY LIST (id)', pg);
    parseOne('CREATE TABLE t (id INT) PARTITION BY HASH (id)', pg);
  });
});

