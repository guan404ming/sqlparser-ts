describe('RENAME', () => {
  test('parse RENAME TABLE (MySQL)', () => {
    parseOne('RENAME TABLE old_name TO new_name', dialects.mysql);
  });
});

describe('Complex DDL scenarios', () => {
  test('parse table with all constraint types', () => {
    parseOne(`
      CREATE TABLE users (
        id INT PRIMARY KEY,
        email VARCHAR(255) UNIQUE NOT NULL,
        age INT CHECK (age >= 18),
        parent_id INT REFERENCES parent(id),
        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
      )
    `);
  });

  test('parse table with composite primary key', () => {
    parseOne('CREATE TABLE t (a INT, b INT, PRIMARY KEY (a, b))');
  });

  test('parse table with multiple foreign keys', () => {
    parseOne(`
      CREATE TABLE t (
        id INT PRIMARY KEY,
        user_id INT REFERENCES users(id),
        product_id INT REFERENCES products(id)
      )
    `);
  });
});
